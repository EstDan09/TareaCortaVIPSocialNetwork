    private void jButtonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarActionPerformed
        String nombreProducto = jTextFieldNombre.getText();
        String descripcionProducto = jTextFieldDescripcion.getText();
        String precioInicialProducto = jTextFieldPrecioInicial.getText();
        String precioFinalProducto = jTextFieldPrecioFinal.getText();
        int precioInicialInt = 0;
        int precioFinalInt = 0;
        try {
            precioInicialInt = Integer.parseInt(precioInicialProducto);
        } catch (NumberFormatException e) {
            System.err.println("Error al convertir el String a un entero: ");
        }
        try {
            precioFinalInt = Integer.parseInt(precioFinalProducto);
        } catch (NumberFormatException e) {
            System.err.println("Error al convertir el String a un entero: ");
        }
        textoAMostar += nombreProducto + "\n";
        txaLog.setText(textoAMostar); 
        try {
            jugador.salida.writeInt(1);
            jugador.salida.writeUTF(textoAMostar);
        } catch (IOException ex) {
            Logger.getLogger(VentanaSubastador.class.getName()).log(Level.SEVERE, null, ex);
        }
        // Mandar producto al server
        try {
            jugador.salida.writeInt(2);
            jugador.salida.writeUTF(nombreProducto);
            jugador.salida.writeUTF(descripcionProducto);
            jugador.salida.writeInt(precioInicialInt);
            jugador.salida.writeInt(precioFinalInt);
        } catch (IOException ex) {
            Logger.getLogger(VentanaSubastador.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonAgregarActionPerformed

